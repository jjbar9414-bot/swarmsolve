// ===== i18n Engine =====

function getCurrentLang() {
  return localStorage.getItem("swarmsolve_lang") || "en";
}

function setLang(lang) {
  localStorage.setItem("swarmsolve_lang", lang);
  applyLang(lang);
}

function applyLang(lang) {
  // Set direction
  const dir = lang === "ar" ? "rtl" : "ltr";
  document.documentElement.setAttribute("dir", dir);
  document.documentElement.setAttribute("lang", lang);
  document.body.classList.toggle("rtl", lang === "ar");

  // Translate all elements with data-i18n
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[key] && translations[key][lang]) {
      el.textContent = translations[key][lang];
    }
  });

  // Translate placeholders
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    const key = el.getAttribute("data-i18n-ph");
    if (translations[key] && translations[key][lang]) {
      el.setAttribute("placeholder", translations[key][lang]);
    }
  });

  // Translate HTML content (for elements with icons inside)
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    if (translations[key] && translations[key][lang]) {
      el.innerHTML = translations[key][lang];
    }
  });

  // Update toggle button
  const toggleBtn = document.getElementById("lang-toggle");
  if (toggleBtn) {
    toggleBtn.textContent = lang === "ar" ? "EN" : "عربي";
  }
}

// Initialize on page load
document.addEventListener("DOMContentLoaded", () => {
  const lang = getCurrentLang();
  applyLang(lang);

  // Add click handler for toggle button
  const toggleBtn = document.getElementById("lang-toggle");
  if (toggleBtn) {
    toggleBtn.addEventListener("click", (e) => {
      e.preventDefault();
      const current = getCurrentLang();
      setLang(current === "en" ? "ar" : "en");
    });
  }

  // ===== HAMBURGER MENU =====
  const hamburger = document.getElementById("hamburger");
  const navLinks = document.getElementById("nav-links");
  if (hamburger && navLinks) {
    hamburger.addEventListener("click", (e) => {
      e.stopPropagation();
      hamburger.classList.toggle("active");
      navLinks.classList.toggle("active");
      // Prevent body scroll when menu is open
      document.body.style.overflow = navLinks.classList.contains("active") ? "hidden" : "";
    });

    // Close menu when clicking any link inside nav
    navLinks.querySelectorAll("a, button:not(#lang-toggle)").forEach((link) => {
      // Skip the lang toggle since it doesn't navigate
      if (link.id === "lang-toggle") return;
      link.addEventListener("click", () => {
        hamburger.classList.remove("active");
        navLinks.classList.remove("active");
        document.body.style.overflow = "";
      });
    });

    // Close menu when clicking outside
    document.addEventListener("click", (e) => {
      if (navLinks.classList.contains("active") && !navLinks.contains(e.target) && !hamburger.contains(e.target)) {
        hamburger.classList.remove("active");
        navLinks.classList.remove("active");
        document.body.style.overflow = "";
      }
    });

    // Close on Escape key
    document.addEventListener("keydown", (e) => {
      if (e.key === "Escape" && navLinks.classList.contains("active")) {
        hamburger.classList.remove("active");
        navLinks.classList.remove("active");
        document.body.style.overflow = "";
      }
    });
  }
});